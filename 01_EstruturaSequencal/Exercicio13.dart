// Construir um programa que leia três valores numéricos e calcular o valor da soma dos seus quadrados.

// Definir os três valores numéricos
void main() {
  double valor1 = 2.5;
  double valor2 = 3.8;
  double valor3 = 4.2;

  // Calcular a soma dos quadrados
  double somaDosQuadrados = valor1 * valor1 + valor2 * valor2 + valor3 * valor3;

  // Apresentar o resultado
  print(somaDosQuadrados);
}