// Elaborar um programa que leia uma medida em pés e realize a conversão para metros.

void main() {
  // Definir a medida em pés
  double medidaEmPes = 10.0; // Exemplo: 10 pés

  // Realizar a conversão para metros (1 pé = 0.3048 metros)
  double medidaEmMetros = medidaEmPes * 0.3048;

  // Apresentar o resultado
  print('$medidaEmPes pés equivalem a $medidaEmMetros metros.');
}
